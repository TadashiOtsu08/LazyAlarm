import SwiftUI

/// アプリ全体で使用するカラー定数
/// カラーコードを直接コードに書かずここから呼び出す
extension Color {
    /// メイン背景色（ダークネイビー #010133）
    static let backgroundPrimary = Color("BackgroundPrimary")
    /// アクセントカラー（ピンク #e8069d）
    static let accentPink = Color("AccentPink")
    /// タブ選択色（#0088ff）
    static let tabSelected = Color("TabSelected")
}
